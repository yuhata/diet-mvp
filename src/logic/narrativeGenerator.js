/**
 * 回答に基づいて表示すべき生物学的コンテキストカードを返す（最大2枚）
 */
export function getBiologicalContextCards(answers) {
  const cards = [];
  for (const ctx of BIO_CONTEXTS) {
    if (ctx.trigger(answers)) {
      cards.push(ctx);
    }
    if (cards.length >= 2) break;
  }
  return cards;
}

